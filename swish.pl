% 1. Plataformas con CPU AMD adquiridas después de 2021
query1 :-
    write('Plataformas con CPU AMD adquiridas después de 2021:'), nl,
    plataforma(Marca, ID, Año, _, 'AMD', _, _, _, _, _),
    Año > 2021,
    write(Marca), write(' - '), write(ID), nl,
    fail.
query1.

% 2. Tablets con más de 2GB de RAM
query2 :-
    write('Tablets con más de 2GB de RAM:'), nl,
    plataforma(Marca, ID, _, RAM, _, _, _, 'Tablet', _, _),
    RAM > 2,
    write(Marca), write(' - '), write(ID), nl,
    fail.
query2.

% 3. Plataformas con discos entre 32GB y 512GB
query3 :-
    write('Plataformas con disco entre 32GB y 512GB:'), nl,
    plataforma(Marca, ID, _, _, _, _, HDD, _, _, _),
    HDD >= 32, HDD =< 512,
    write(Marca), write(' - '), write(ID), nl,
    fail.
query3.

% 4. Cuántas plataformas son ASUS
query4 :-
    findall(ID, plataforma('ASUS', ID, _, _, _, _, _, _, _, _), Lista),
    length(Lista, Cant),
    write('Cantidad de plataformas ASUS: '), write(Cant), nl.

% 5. Laptops con más de 4GB RAM y menos de 512GB HDD
query5 :-
    write('Laptops con más de 4GB RAM y menos de 512GB HDD:'), nl,
    plataforma(Marca, ID, _, RAM, _, _, HDD, 'Laptop', _, _),
    RAM > 4, HDD < 512,
    write(Marca), write(' - '), write(ID), nl,
    fail.
query5.

